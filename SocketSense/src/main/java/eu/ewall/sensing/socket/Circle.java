package eu.ewall.sensing.socket;

public class Circle {
	private String mac;
	private String category;
	private String name;
	private String loginterval;
	private String always_on;
	private String production;
	private String location;
	
	public String getMac() {
		return mac;
	}
	public void setMac(String mac) {
		this.mac = mac;
	}
	public String getCategory() {
		return category;
	}
	public void setCategory(String category) {
		this.category = category;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getLoginterval() {
		return loginterval;
	}
	public void setLoginterval(String loginterval) {
		this.loginterval = loginterval;
	}
	public String getAlways_on() {
		return always_on;
	}
	public void setAlways_on(String always_on) {
		this.always_on = always_on;
	}
	public String getProduction() {
		return production;
	}
	public void setProduction(String production) {
		this.production = production;
	}
	public String getLocation() {
		return location;
	}
	public void setLocation(String location) {
		this.location = location;
	}

}
